package edu.borzhch.constants;

/**
 * Типы языка Borzhch
 * @author Balushkin M.
 */
public enum BOType {
    /**
     * void, не используется в явном виде
     */
    VOID,
    /**
     * Логический тип
     */
    BOOL,
    /**
     * Целочисленный тип
     */
    INT,
    /**
     * Число с плавающей точкой
     */
    FLOAT,
    /**
     * Строка
     */
    STRING,
    /**
     * Ссылочный тип
     */
    REF
}
